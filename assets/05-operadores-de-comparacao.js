// Os operadores de comparação são usados para comparar dois valores e retornar um valor booleano (true ou false) com base na comparação.

// Os operadores de comparação mais comuns são:

// == (igualdade) - Verifica se dois valores são iguais, ignorando o tipo de dado.

let a = 5; //number
let b = '55'; //string

console.log(typeof a); // number
console.log(typeof b); // string

console.log(a == b); // false

// === (estritamente igual) - Verifica se dois valores são iguais, considerando o tipo de dado.
console.log(a === b);

// != (diferente) - Verifica se dois valores são diferentes, ignorando o tipo de dado.
console.log(a != b); // true

// !== (estritamente diferente) - Verifica se dois valores são diferentes, considerando o tipo de dado.
console.log(a !== b); // true